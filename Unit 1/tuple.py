#Create a tuple
t1 = (10,20,30)
print("Tuple:",t1)

#Access elements
print("First element:",t1[0])

#Nested tuple
t2 = (1,2,(3,4))
print("Nested Tuple:",t2)

#Repetition of tuple
t3=t1*2
print("Repeated Tuple:",t3)

#Concatenation of tuples
t4=t1+(40,50)
print("Concatenated Tuple:",t4)
